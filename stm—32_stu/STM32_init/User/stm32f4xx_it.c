void EXTI15_10_IRQHandler(void)
{
  if(EXTI_GetITStatus(EXTI_Line11) == SET)
  {
    GPIO_ResetBits(GPIOC , GPIO_Pin_0);
    
    EXTI_ClearITPendingBit(EXTI_Line11);
  }
}

